package models;

/**
* <h1>Image Model</h1>
* Model for the image.
*
*/
public class Image {

	private int id;
	private String path;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
}
